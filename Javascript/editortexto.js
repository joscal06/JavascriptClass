function escritura()
{
    var input1 = document.getElementById('text1');
    var input2 = document.getElementById('text2');
    var salida = document.getElementById('textosalida');

    salida.textContent = input1.value;
    salida.style = input2.value;
    salida.style.color = "Green";
}

